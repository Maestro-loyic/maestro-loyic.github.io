function applyLanguage(lang) {
  var dict = i18n[lang] || i18n.en;
  document.documentElement.setAttribute('lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });
  var label = document.getElementById('langBtnLabel');
  if (label) label.textContent = lang.toUpperCase();
  document.querySelectorAll('.lang-menu button').forEach(function(b) {
    b.classList.toggle('active-lang', b.getAttribute('data-lang') === lang);
  });
  try { localStorage.setItem('elimu_lang', lang); } catch (e) {}
}

(function initLangSwitcher() {
  var savedLang = 'en';
  try { savedLang = localStorage.getItem('elimu_lang') || 'en'; } catch (e) {}
  var langBtn = document.getElementById('langBtn');
  var langMenu = document.getElementById('langMenu');
  if (langBtn && langMenu) {
    langBtn.addEventListener('click', function(e) {
      e.stopPropagation();
      langMenu.classList.toggle('open');
    });
    langMenu.querySelectorAll('button').forEach(function(btn) {
      btn.addEventListener('click', function() {
        applyLanguage(btn.getAttribute('data-lang'));
        langMenu.classList.remove('open');
      });
    });
    document.addEventListener('click', function() {
      langMenu.classList.remove('open');
    });
  }
  if (savedLang !== 'en') applyLanguage(savedLang);
  else { var l = document.getElementById('langBtnLabel'); if (l) l.textContent = 'EN'; }
})();

