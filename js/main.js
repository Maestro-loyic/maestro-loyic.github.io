(function initStoriesCarousel() {
  var track = document.querySelector('#storiesCarousel .stories-track');
  if (!track) return;
  var slides = track.querySelectorAll('.story-slide');
  var dots = document.querySelectorAll('#storyDots .story-dot');
  var prevBtn = document.getElementById('storyPrev');
  var nextBtn = document.getElementById('storyNext');
  var current = 0;
  var timer;

  function goTo(index) {
    slides[current].classList.remove('active');
    dots[current].classList.remove('active');
    current = (index + slides.length) % slides.length;
    slides[current].classList.add('active');
    dots[current].classList.add('active');
  }

  function next() { goTo(current + 1); }
  function prev() { goTo(current - 1); }

  function startAuto() {
    clearInterval(timer);
    timer = setInterval(next, 6500);
  }

  if (nextBtn) nextBtn.addEventListener('click', function() { next(); startAuto(); });
  if (prevBtn) prevBtn.addEventListener('click', function() { prev(); startAuto(); });
  dots.forEach(function(dot, i) {
    dot.addEventListener('click', function() { goTo(i); startAuto(); });
  });

  startAuto();
})();

